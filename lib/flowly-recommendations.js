/** Общая логика рекомендаций: локальный Express и Vercel /api/recommendations. */
export function buildRecommendations({ phase, mood, energy, stress, symptoms }) {
  const list = [];
  const s = symptoms || {};

  if (phase === "menstrual") {
    list.push({
      title: "Нежная активность",
      body: "Лёгкая йога или прогулка снижают дискомфорт и поддерживают настроение.",
      tag: "активность",
    });
    list.push({
      title: "Тепло и отдых",
      body: "Тёплая грелка, достаточный сон и железо/белок в питании — хорошая база.",
      tag: "здоровье",
    });
  } else if (phase === "follicular") {
    list.push({
      title: "Рост энергии",
      body: "Хорошее время для силовых тренировок или новых задач — организм чаще восстанавливается быстрее.",
      tag: "активность",
    });
  } else if (phase === "ovulation") {
    list.push({
      title: "Социальность и движение",
      body: "Пик коммуникабельности — планируйте важные разговоры и командную работу.",
      tag: "эмоции",
    });
  } else {
    list.push({
      title: "Ритуалы спокойствия",
      body: "Вторая фаза — время для медитации, дыхания 4-7-8 и снижения кофеина к вечеру.",
      tag: "медитация",
    });
  }

  if (Number(stress) >= 4) {
    list.push({
      title: "Стресс",
      body: "Короткая прогулка 10 минут или дневник благодарности помогают снизить напряжение.",
      tag: "эмоции",
    });
  }
  if (Number(energy) <= 2) {
    list.push({
      title: "Энергия",
      body: "Дробное питание, сложные углеводы и гидратация — мягкая поддержка без перегруза.",
      tag: "питание",
    });
  }
  if (Number(mood) <= 2) {
    list.push({
      title: "Настроение",
      body: "Мини-ритуал: любимый плейлист, тёплый душ, связь с близким человеком.",
      tag: "эмоции",
    });
  }

  if (s.endo?.length) {
    list.push({
      title: "Эндометриоз",
      body: "Отмечайте триггеры боли и обсудите план с врачом — дневник симптомов помогает на приёме.",
      tag: "здоровье",
    });
  }
  if (s.pcos?.length) {
    list.push({
      title: "СПКЯ",
      body: "Регулярные интервалы еды, белок к завтраку и сон 7–8 ч часто улучшают самочувствие.",
      tag: "питание",
    });
  }
  if (s.pms?.length) {
    list.push({
      title: "ПМС",
      body: "Магний и умеренная кардионагрузка иногда облегчают симптомы — уточняйте дозировки с врачом.",
      tag: "здоровье",
    });
  }

  return list.slice(0, 8);
}
